package day03_escape_sequence;

public class EmployeeInfo {
    public static void main(String[] args){
        System.out.println("Company name:   Google  \nEmployee:       Anna \nID:             12345 \nJob Title:      CEO");
        System.out.println();
        System.out.println("Company name:\tGoogle\nEmployee:\t\tAnna\nID:\t\t\t\t12345\nJob Title:\t\tCEO");
    }
}
